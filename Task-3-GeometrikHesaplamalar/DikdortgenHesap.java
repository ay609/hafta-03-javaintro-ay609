public class Dikdortgen {
    public static void main(String[] args) {
        double en = 4.5, boy = 7.9;

        double alan = en * boy;
        double cevre = 2 * (en + boy);

        System.out.println("Dikdörtgen Alanı : " + alan);
        System.out.println("Dikdörtgen Çevresi : " + cevre);
    }
}
