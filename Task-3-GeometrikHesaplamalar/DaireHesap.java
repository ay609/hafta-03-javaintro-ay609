public class DaireHesap {
    public static void main(String[] args) {
        double r = 5.5;
        double pi = Math.PI;

        double alan = pi * r * r;
        double cevre = 2 * pi * r;

        System.out.println("Yarıçap : " + r);
        System.out.println("Alan : " + alan);
        System.out.println("Çevre : " + cevre);
    }
}
