public class Donusum {
    public static void main(String[] args) {
        double oran = 1.609;

        System.out.println("Mil\tKilometre");
        System.out.println("----------------");
        System.out.println("1\t" + (1 * oran));
        System.out.println("5\t" + (5 * oran));
        System.out.println("10\t" + (10 * oran));
        System.out.println("20\t" + (20 * oran));
        System.out.println("50\t" + (50 * oran));
    }
}
